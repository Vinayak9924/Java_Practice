package vinayak1;

public class Encapsulation {
 private int accno;
 private String name;
 private double amount;
public int getAccno() {
	return accno;
}
public String getName() {
	return name;
}
public double getAmount() {
	return amount;
}
public void setAccno(int accno) {
	this.accno = accno;
}
public void setName(String name) {
	this.name = name;
}
public void setAmount(double amount) {
	this.amount = amount;
}
  
 
 
 
 }
